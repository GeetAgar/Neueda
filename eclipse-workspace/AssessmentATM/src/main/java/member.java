
public class member {
	
	private String accno, pin;

	public member() {
		super();
	}

	public member(String accno, String pin) {
		super();
		this.accno = accno;
		this.pin = pin;
	}

	public String getAccno() {
		return accno;
	}

	public void setAccno(String accno) {
		this.accno = accno;
	}

	public String getPin() {
		return pin;
	}

	public void setPin(String pin) {
		this.pin = pin;
	}
	
	
	

}
